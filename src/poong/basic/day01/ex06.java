package poong.basic.day01;

public class ex06 {
	public static void main(String[] args) {

		// 5.

		double x = 2.5;
		double y = 1.5;
		int m = 18;
		int n = 4;

		System.out.print("n*y = ");
		System.out.println(n * y);
		System.out.print("가. x + n*y - (x+n) *y = ");
		System.out.println(x + n * y - (x + n) * y);

		System.out.print("m/n = ");
		System.out.println(m / n);
		System.out.print("m%n = ");
		System.out.println(m % n);
		System.out.print("나. m/n + m%n = ");
		System.out.println(m / n + m % n);

		System.out.print("다. 5*x - n/5 = ");
		System.out.println(5 * x - n / 5);

		System.out.print("라. 1 - (1- (1- (1- (1 - n)))) = ");
		System.out.println(1 - (1 - (1 - (1 - (1 - n)))));

	}
}
